package week1.day1;

public class OddorEven {
	
	public static void main(String[] args) {
		
		
		for (int i = 1; i < 20; i++) {
			
			if(i%2==0) {
				
				
				System.out.println(i+"odd number");
			}
			else {
				
				
				System.out.println(i +"even number");
			}
			
		}
	}

}
