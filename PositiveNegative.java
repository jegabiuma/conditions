package week1.day2;

public class PositiveNegative {

	public static void main(String[] args) {
		
		int num=0;
		if (num<0) {
			
			System.out.println(num+ " is negative");
			
		}
		else if (num>0) {
			
			System.out.println(num+ " is positive");
			
		} 
		else
			
		
		{
			System.out.println(num+ " number is neither positive nor negative");
		}

	}

}
